<p>
  Karbon yatakları zamanla doygunluğa ulaşır; çıkışta koku/VOC artar. Sürdürülebilir performans için <strong>ΔP</strong>
  ve/veya <strong>VOC sensörleri</strong> ile izleme yapılmalı; ΔP tek başına doygunluğu her zaman göstermez. Tipik değişim
  aralığı endüstride 3–9 ay, HVAC uygulamalarında 6–12 aydır. Nem/sıcaklık artışı bazı gazlarda adsorpsiyonu düşürür;
  kondenz yatak performansını bozar. Düzenli ön filtre değişimi karbon ömrünü belirgin uzatır.
</p>
<p>
  Değişimde atık yönetimi mevzuatına uyulmalı; doygun karbon içerdiği kirleticiye göre tehlikeli atık sınıfında olabilir.
  Kayıt tutarak (giriş konsantrasyonu, debi, T/RH, değişim tarihleri) bir sonraki medya seçimi ve yatak kalınlığı daha iyi
  optimize edilir.
</p>
