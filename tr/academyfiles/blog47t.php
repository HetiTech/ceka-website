<p>
  CEKA Endüstriyel, vakum pompaları için <strong>modüler kapalı devre soğutma</strong> çözümleri sunar: 
  PE/PP <em>buffer</em> tanklar, AISI 316L plakalı/borulu eşanjörler, otomatik yan akış filtrasyonu, PID kontrollü vana/fan, 
  uzaktan izleme ve saha koşullarına uygun montaj ekipmanı. Proses suyu kimyasına göre conta/kaplama seçenekleri ile uzun ömür hedeflenir.
</p>
<p>
  Anahtar teslim yaklaşım: ısı yükü hesabı, hat şeması ve kolektör tasarımı, sensör yerleşimi (TT/PT/FT), elektrik-otomasyon, 
  devreye alma ve eğitim. İsteğe bağlı olarak kış şartları için glikol optimizasyonu, yedek pompa/ikizleme ve bakım sözleşmesi sunulur.
</p>
