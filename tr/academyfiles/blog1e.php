  <h2 class="text-2xl font-bold mt-6 mb-2">What is a RAS System?</h2>
  <p>RAS is a <strong>closed-loop aquaculture system</strong> where water is continuously reused after passing through mechanical and biological filtration. Unlike traditional fish farms that require constant water input and discharge, RAS systems recirculate and purify the same water, minimizing consumption and significantly reducing environmental impact.</p>

  <p>These systems operate like a <strong>self-sustaining aquatic ecosystem</strong>. Solid wastes are removed by mechanical filters, ammonia is broken down by biofiltration, oxygen levels are maintained, and CO₂ gas is removed before water returns to the tanks.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">Why Choose RAS?</h2>
  <ul class="list-disc pl-6">
    <li>Up to 99% water savings</li>
    <li>High biosecurity through isolated and controllable environment</li>
    <li>Production possible in urban or indoor spaces due to smaller footprint</li>
    <li>Year-round production regardless of seasonal conditions</li>
    <li>Advantage of not discharging waste directly into natural ecosystems</li>
  </ul>

  <h2 class="text-2xl font-bold mt-6 mb-2">Applications</h2>
  <ul class="list-disc pl-6">
    <li>Urban or inland fish farms</li>
    <li>Hatcheries and research centers</li>
    <li>High-value species like trout and sea bass or ornamental fish</li>
    <li>Export-oriented production facilities requiring traceability and hygiene</li>
  </ul>

