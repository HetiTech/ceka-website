<p>
  Doğru seçim için hedef kirletici(ler), konsantrasyon, saatlik debi ve mevzuat limitleri belirlenmeli; buna göre
  <em>EBCT</em> hesaplanmalıdır. EBCT arttıkça verim yükselir; ancak hacim/ΔP artabilir. Hindistan cevizi kabuğu bazlı
  karbonlar VOC’lerde; kimyasal emdirilmiş karbonlar asidik/bazik gazlarda öne çıkar. Kartuş/kaset mimarisi bakım kolaylığı,
  dökme yatak yüksek debide maliyet avantajı sağlar. Girişte mutlaka partikül ön filtresi ve yoğun aerosol varsa koalesan
  aşaması önerilir.
</p>
<p>
  Enerji verimliliği için basınç kaybı–verim dengesi kurulmalı; servis aralığı ve ΔP/sensör izleme baştan planlanmalıdır.
</p>
