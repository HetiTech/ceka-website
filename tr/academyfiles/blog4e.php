  <h2 class="text-2xl font-bold mt-6 mb-2">Monitoring and Control</h2>
  <p>Modern RAS facilities utilize real-time sensors to monitor these parameters 24/7. Automation systems can trigger alarms or corrective actions such as oxygen injection, pH adjustment, or water replacement when limits are exceeded.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">Best Practices</h2>
  <ul class="list-disc pl-6">
    <li>Daily calibration of pH and DO sensors</li>
    <li>Backup oxygen supply systems</li>
    <li>Routine testing for ammonia and nitrites</li>
    <li>Maintaining a stable biofilter population</li>
    <li>Temperature alarms and heating/cooling integration</li>
  </ul>

  <h2 class="text-2xl font-bold mt-6 mb-2">Conclusion</h2>
  <p>Water quality is the foundation of fish health in RAS. Through consistent monitoring and proactive management, farmers can ensure a productive and disease-free aquaculture environment.</p>
