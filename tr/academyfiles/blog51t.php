<p>
  PP ve PE, kimyasal proses tanklarında en çok kullanılan iki termoplastiktir. 
  Kimyasal dayanım, sıcaklık, darbe direnci ve uygulama koşulları malzeme seçiminde belirleyicidir.
</p>

<p>
  PP, asit-baz proseslerinde yüksek dayanım sunarken, PE deniz suyu ve düşük sıcaklık uygulamalarında daha kararlıdır. 
  Gıda proseslerinde PE100 tercih edilir. PP ise yüksek sıcaklığa dayanımı nedeniyle CIP sistemli tanklarda idealdir.
</p>

