<p>
  Proses ekipman entegrasyonu tank tasarımının son aşaması değil; başlangıç kriteridir. 
  Karıştırıcı, seviye sensörü, debimetre, numune noktası ve pompa hattı yerleşimi akış davranışını doğrudan etkiler.
</p>

<p>
  Karıştırıcı kullanılan tanklarda gövdeye binen tork yükü sebebiyle metal gömlek zorunludur. Sensörler türbülans noktalarından 
  uzak ve servis edilebilir konumda olmalıdır. Pompa hattı çıkışları genellikle konik taban merkezinden alınır.
</p>

