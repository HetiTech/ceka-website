<p>
  PP ve PE malzemeler yüksek ısı genleşmesi katsayısına sahiptir. 
  Bu nedenle uzun tanklarda termal genleşme payı bırakılmalı ve UV dayanımlı plakalar tercih edilmelidir.
</p>

<p>
  UV katkısı olmayan PE/PP dış ortamlarda 3–5 yıl içinde renk solması ve gevrekleşme yaşar. CEKA tüm dış ortam tanklarında UV-stabilize plakalar kullanır.
</p>

