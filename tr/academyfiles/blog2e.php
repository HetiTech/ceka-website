<div class="text-base text-gray-700 leading-relaxed">
  <p class="mb-4">Every Recirculating Aquaculture System (RAS) relies on a set of integrated components that work together to create a healthy, stable environment for fish. Understanding how each component functions is key to successful system design and operation.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">1. Mechanical Filtration</h2>
  <p>Removes solid waste particles like uneaten food and feces. Common types include drum filters, bead filters, and settling tanks.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">2. Biological Filtration</h2>
  <p>Converts harmful ammonia and nitrites into less toxic nitrates via nitrifying bacteria. Typically housed in biofilters or moving bed reactors (MBBR).</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">3. Oxygenation and Degassing</h2>
  <p>Oxygen is added through oxygen cones, diffusers, or packed columns. CO₂ and nitrogen gases are removed using degassers to maintain optimal water chemistry.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">4. Disinfection</h2>
  <p>UV sterilizers or ozone generators are used to eliminate pathogens, improving biosecurity and reducing disease outbreaks.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">5. Monitoring & Automation</h2>
  <p>Modern RAS setups use sensors and control systems (e.g. PLC panels) to track temperature, pH, oxygen, and flow rate in real-time, reducing human error and improving consistency.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">Conclusion</h2>
  <p>Each of these components must be properly sized and integrated. A weak link in one part can compromise the entire system. That’s why understanding RAS components is essential to building a reliable and scalable fish farming operation.</p>
</div>
