<p>
  Uygulamada en sık karşılaşılan tipler: <strong>halka sulu (liquid ring) vakum pompaları</strong>, <strong>yağlı döner paletli</strong> ve <strong>kuru vidalı</strong> pompalar. 
  Halka sulu tiplerde “seal water” kalitesi ve sıcaklığı performansı direkt etkiler; kapalı devre ile bu parametreler sabitlenir. 
  Yağlı ve kuru tiplerde ise gövde/yağ soğutma ceketleri kararlı sıcaklıkta tutulur, sızdırmazlık elemanlarının ömrü uzar.
</p>
<p>
  Tipik entegrasyon: pompa çıkış ceketinden dönen akış, <em>buffer tank</em> → <em>pompa</em> → <em>eşanjör</em> → <em>geri dönüş</em> sırasını izler. 
  Bypass hattı ile servis modunda akış kesintisiz sürdürülür; TT-1 (pompa giriş), TT-2 (pompa çıkış) sıcaklıkları izlenir. 
  Yan akış filtre, iletkenlik/su kalitesi sensörü ve otomatik takviye (yalnızca buharlaşma/kayıplar) önerilir.
</p>
