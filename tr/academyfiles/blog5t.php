  <h2 class="text-2xl font-bold mt-6 mb-2">Arıtma Yöntemleri</h2>
  <p>RAS’te su arıtımı, güvenli koşulları korumak için çeşitli teknolojilerin kombinasyonunu içerir:</p>
  <ul class="list-disc list-inside mt-2 mb-4">
    <li><strong>Mekanik filtrasyon</strong>: Askıda katı maddeleri uzaklaştırır</li>
    <li><strong>Biyofiltrasyon</strong>: Amonyağı nitrifikasyon bakterileriyle nitrata dönüştürür</li>
    <li><strong>Ozon veya UV dezenfeksiyonu</strong>: Patojenleri kontrol altına alır ve organik yükü azaltır</li>
    <li><strong>Gaz giderimi ve havalandırma</strong>: CO₂’yi uzaklaştırır ve oksijen seviyesini dengeler</li>
  </ul>

  <h2 class="text-2xl font-bold mt-6 mb-2">Sonuç</h2>
  <p>Otomatik sensörler ve alarm sistemleriyle desteklenen tutarlı izleme, erken müdahale sağlar ve RAS işletmelerinde biyogüvenlik ile balık sağlığını en üst düzeye çıkarır.</p>
