  <p><strong>Başlık:</strong> RAS'te Su Kalitesinin Korunması: İzleme ve Arıtma Stratejileri</p>

  <p class="mt-4">Resirkülasyonlu Akuakültür Sistemleri (RAS) için optimum su kalitesini korumak temel bir gerekliliktir. Balık sağlığı ve büyümesi doğrudan su koşullarına bağlı olduğundan, sürekli izleme ve etkili arıtma stratejileri büyük önem taşır.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">İzlenmesi Gereken Temel Su Parametreleri:</h2>
  <p>Bir dizi fizikokimyasal parametre, balık refahını doğrudan etkiler. Bunlar şunlardır:</p>
  <ul class="list-disc list-inside mt-2 mb-4">
    <li><strong>Sıcaklık</strong>: Metabolizma hızları için hayati önemdedir</li>
    <li><strong>Çözünmüş Oksijen (DO)</strong>: Solunum için kritik öneme sahiptir</li>
    <li><strong>pH</strong>: Biyolojik işlevleri ve diğer bileşiklerin toksisitesini etkiler</li>
    <li><strong>Amonyak (NH₃/NH₄⁺)</strong>: Serbest formda son derece toksiktir</li>
    <li><strong>Nitrit (NO₂⁻) ve Nitrat (NO₃⁻)</strong>: Biyofiltrenin verimliliğini gösterir</li>
  </ul>

