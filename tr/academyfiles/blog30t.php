<p>
  Tek bir filtrasyon teknolojisi tüm ihtiyaçları karşılamaz. Aktif karbon gaz/koku gideriminde üstün; <strong>HEPA</strong>
  partiküllerde, <strong>elektrostatik</strong> yağ-sis aerosollerinde ve <strong>UV/PCO</strong> mikroorganizmalarda güçlüdür.
  Genelde hibrit yaklaşım uygulanır: ön filtre (G4–M5), gerekiyorsa HEPA ve gaz fazı için aktif karbon yatağı.
</p>
<p>
  HEPA ≥0,3 µm partiküllerde yüksek verim sunar fakat gaz tutamaz. UV mikroorganizma inaktivasyonunda etkilidir; koku gidermez.
  Elektrostatik çöktürücü partikülerde iyidir, gaz yönetimi sınırlıdır. Hedef VOC/koku ise aktif karbon vazgeçilmezdir;
  partikül hedefliyse HEPA gereklidir. Seçim, hedef kirleticinin yükü ve proses koşullarına göre yapılır.
</p>
