<p>
  PP/PE tanklarda gövde yükleri tamamen sıvı kolonundan kaynaklanır. 
  5000 L üzeri tanklarda metal şase, ayak veya kuşak zorunludur. Dinamik sistemlerde (karıştırıcı + pompaj) yükler katlanarak artar.
</p>

<p>
  Şase tasarımında yük dağılımı, çerçeve rijitliği, termal genleşme ve titreşim sönümleme birlikte değerlendirilir.
</p>

