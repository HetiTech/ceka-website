<p>
  Depolama malzemesi seçimi, kullanım sıcaklığı, basınç, medya uyumluluğu ve işletme stratejisine göre yapılmalıdır.
  Metal tanklar yüksek sıcaklık/basınç içeren özel uygulamalarda avantajlıdır; buna karşın atmosferik ve saha depolamalarında
  plastik tanklar maliyet, bakım ve sızdırmazlık yönleriyle öne çıkar. Proje başlangıcında medya uyumluluk kontrolü, ikincil
  havuzlama ve havalandırma tasarımı birlikte ele alınmalıdır.
</p>
