<p>
  Aktif karbon hava filtresi, havadaki koku, VOC ve belirli gazları gidermek için karbonun gözenekli yapısından yararlanır.
  Temel mekanizma <strong>adsorpsiyon</strong>dur; gaz molekülleri karbon yüzeyine tutunur. Mikro–mezo–makro gözenekli
  yapı farklı molekül boyutlarını yakalar. 1 g aktif karbonun yüzey alanı binlerce m²’ye ulaşabilir; bu sayede kompakt hacimde
  yüksek kapasite elde edilir. Uygulamada GAC, pelet veya emdirilmiş keçe formları kullanılır ve çoğu zaman ön/HEPA filtreyle
  birlikte çözüm tasarlanır.
</p>
<p>
  Performansı etkileyen ana parametreler: karbon türü (kabuk/kömür), iyot sayısı/yüzey alanı, gözenek dağılımı, yatak kalınlığı,
  <em>Empty Bed Contact Time</em> (EBCT), sıcaklık ve nemdir. Hedef gazın polaritesi ve konsantrasyonu doygunluk hızını belirler.
  Boya kabinleri, laboratuvar davlumbazları, kimyasal depolama ve HVAC gibi alanlarda iç hava kalitesini belirgin iyileştirir.
</p>
