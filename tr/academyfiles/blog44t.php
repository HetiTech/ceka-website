<p>
  Kapalı devre soğutma; su/karışımın bir <strong>eşanjör</strong> üzerinden proses yükünden ısıyı alıp geri döndüğü, dış ortamla kontrollü ısı alışverişi yaptığı bir devredir. 
  Temel bileşenler: <strong>eşanjör (plakalı/borulu)</strong>, <strong>sirkülasyon pompası</strong>, <strong>buffer tank</strong>, <strong>genleşme tankı</strong>, <strong>otomasyon ve yan filtrasyon</strong>. 
  Sabit bir set sıcaklığı (örn. 25–30 °C) hedeflenir; PID kontrollü vana veya fanlı soğutucu ile hassas regülasyon sağlanır.
</p>
<p>
  Vakum pompaları için kritik noktalar: yeterli <em>akış hızı</em> ve <em>Reynolds</em> ile ısı transferinin iyileştirilmesi, <em>hidrolik kısa devre</em> riskinin önlenmesi, 
  korozyon-kimyasal uyumluluk (PE/PP tank, AISI 316L eşanjör), <em>glikol yüzdesi</em> (don/yoğuşma şartlarına göre) ve <em>yan akış filtrasyonu</em> ile berraklığın korunmasıdır.
</p>
