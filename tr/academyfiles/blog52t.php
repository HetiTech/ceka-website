<p>
  Manhole, flanş ve bağlantı ekipmanlarının yerleşimi tankın bakım kolaylığı, akış optimizasyonu ve işletme güvenliği açısından 
  en kritik tasarım unsurlarındandır. Yanlış konumlandırma ölü hacim, türbülans ve lokal gerilme oluşturur.
</p>

<p>
  CEKA’nın standart yaklaşımı: akış yönüne uygun, simetrik, erişilebilir ve bakım sırasında ekipmanın güvenli şekilde çıkarılabileceği 
  konumlandırmadır. 300–450 mm manhole boyutları yaygın kullanılır.
</p>

