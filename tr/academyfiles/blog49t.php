<p>
  Endüstriyel PP/PE tanklarda konik veya düz taban seçimi, proses davranışı ve drenaj ihtiyacına göre belirlenir. 
  Yanlış seçim ölü hacim, çökelti birikimi, temizlik zorlukları ve pompa verim kaybı yaratır.
</p>

<p>
  Konik tabanlar genellikle 15–60° açıyla uygulanır. Kimyasal olmayan temiz proseslerde 15–25°, çökelti tutan sistemlerde 
  45–60° önerilir. Düz taban düşük maliyetlidir ancak tam boşaltma gerektirmeyen uygulamalara uygundur.
</p>

