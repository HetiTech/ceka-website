<p>
  UV sistemleri, kimyasal satın alımını ve depolama–dozaj altyapılarını azaltarak işletme maliyetlerinde önemli kazanımlar sağlar. 
  Temel gider kalemi elektrik tüketimi ve lamba/kuvars kılıf gibi sarf malzemeleridir. 
  Düşük basınçlı (LP) lambalar daha düşük güçte çalışırken, orta basınçlı (MP) lambalar daha kompakt kurulum olanağı verir; doğru seçim debi, UVT ve alan kısıtlarına bağlıdır.
</p>
<p>
  Yaşam döngüsü maliyeti (LCC) çalışmaları yapılırken; enerji birim maliyeti, yıllık çalışma saati, lamba ömrü (çalışma saati), kuvars kılıf temizliği ve sensör kalibrasyonu birlikte değerlendirilmelidir. 
  Ön arıtmanın iyileştirilmesi UVT’yi artırdığından gerekli doz için enerji gereksinimini düşürür. 
  Otomatik kimyasal temizleme (wiper) ve akıllı sürücülerle (step dimming) verimlilik daha da artırılabilir.
</p>
