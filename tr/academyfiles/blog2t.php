  <p class="mb-4">Her Resirkülasyonlu Akuakültür Sistemi (RAS), balıklar için sağlıklı ve dengeli bir ortam oluşturmak amacıyla birlikte çalışan entegre bileşenlerden oluşur. Her bileşenin nasıl çalıştığını anlamak, başarılı bir sistem tasarımı ve işletmesi için kritiktir.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">1. Mekanik Filtrasyon</h2>
  <p>Yenmemiş yem ve dışkı gibi katı atık partiküllerini uzaklaştırır. Yaygın kullanılan türler arasında tambur filtreler, boncuk filtreler ve çökelme tankları yer alır.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">2. Biyolojik Filtrasyon</h2>
  <p>Zararlı amonyak ve nitritleri, nitrifikasyon yapan bakteriler aracılığıyla daha az toksik nitratlara dönüştürür. Genellikle biyofiltrelerde veya hareketli yatak reaktörlerinde (MBBR) bulunur.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">3. Oksijenleme ve Gaz Giderimi</h2>
  <p>Suya oksijen, oksijen konileri, difüzörler veya dolgulu kolonlar aracılığıyla eklenir. CO₂ ve azot gazları ise degazörler kullanılarak ortamdan uzaklaştırılır ve su kimyası dengede tutulur.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">4. Dezenfeksiyon</h2>
  <p>Patojenleri ortadan kaldırmak için UV sterilizatörler veya ozon jeneratörleri kullanılır. Bu, biyogüvenliği artırır ve hastalık salgınlarını azaltır.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">5. İzleme ve Otomasyon</h2>
  <p>Modern RAS sistemleri, sıcaklık, pH, oksijen ve debi gibi değerleri anlık olarak izlemek için sensörler ve kontrol sistemleri (örneğin PLC panelleri) kullanır. Bu, insan hatasını azaltır ve üretim sürecinde istikrar sağlar.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">Sonuç</h2>
  <p>Bu bileşenlerin her biri doğru boyutlandırılmalı ve entegre edilmelidir. Sistemin herhangi bir parçasındaki zayıflık, tüm yapıyı riske atabilir. Bu nedenle RAS bileşenlerini anlamak, güvenilir ve ölçeklenebilir bir balık yetiştiriciliği operasyonu kurmanın temelidir.</p>
