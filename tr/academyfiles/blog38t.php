<p>
  Ultraviyole (UV) dezenfeksiyon, su içerisindeki mikroorganizmaların DNA/RNA yapısını bozarak üremelerini engelleyen, kimyasal madde eklemeden çalışan bir teknolojidir. 
  Suyun tat, koku ve iletkenlik gibi temel karakteristiklerini değiştirmez; bu yönüyle içme suyu, proses suyu ve geri kazanım hatlarında güvenle tercih edilir. 
  UV sistemleri genellikle mekanik (kartuş/torba) ve aktif karbon filtrasyonundan sonra “son dezenfeksiyon aşaması” olarak kurgulanır; böylece bulanıklık azaltılmış ve ışık geçirgenliği artırılmış suya optimum doz uygulanabilir.
</p>
<p>
  Pratikte tasarımın temeli, hedeflenen mikroorganizma inaktivasyonu için “UV dozunu (mJ/cm²)” doğru seçmektir. Debi, UVT (%Transmittance), sıcaklık ve hidrolik düzen (hidrolik kısa devreyi önleme) kritik parametrelerdir. 
  Düşük basınç (LP) ve orta basınç (MP) lambalar arasında dalga boyu spektrumu, enerji tüketimi ve ayak izi açısından farklılıklar bulunur. 
  Endüstriyel uygulamalarda uzaktan izleme, saatlik/kümülatif enerji ve lamba ömrü takibi; bakım planlaması ve sürdürülebilirlik hesabı için önemlidir.
</p>
