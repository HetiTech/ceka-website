  <h2 class="text-2xl font-bold mt-6 mb-2">Treatment Approaches</h2>
  <p>Water treatment in RAS involves a combination of technologies to maintain safe conditions:</p>
  <ul class="list-disc list-inside mt-2 mb-4">
    <li><strong>Mechanical filtration</strong>: Removes suspended solids</li>
    <li><strong>Biofiltration</strong>: Converts ammonia to nitrate via nitrifying bacteria</li>
    <li><strong>Ozone or UV disinfection</strong>: Controls pathogens and reduces organic load</li>
    <li><strong>Degassing and aeration</strong>: Removes CO₂ and balances oxygen levels</li>
  </ul>

  <h2 class="text-2xl font-bold mt-6 mb-2">Conclusion</h2>
  <p>Consistent monitoring supported by automated sensors and alarms enables early intervention and maximizes biosecurity and fish health in RAS operations.</p>
