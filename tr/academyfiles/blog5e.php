  <p><strong>Title:</strong> Maintaining Water Quality in RAS: Monitoring and Treatment Strategies</p>

  <p class="mt-4">Maintaining optimal water quality is a cornerstone of Recirculating Aquaculture Systems (RAS). Since fish health and growth are directly tied to water conditions, continuous monitoring and efficient treatment strategies are essential.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">Key Water Parameters to Monitor:</h2>
  <p>Several physicochemical parameters influence fish welfare. These include:</p>
  <ul class="list-disc list-inside mt-2 mb-4">
    <li><strong>Temperature</strong>: Vital for metabolic rates</li>
    <li><strong>Dissolved Oxygen (DO)</strong>: Crucial for respiration</li>
    <li><strong>pH</strong>: Affects biological functions and toxicity of other compounds</li>
    <li><strong>Ammonia (NH₃/NH₄⁺)</strong>: Highly toxic in free form</li>
    <li><strong>Nitrite (NO₂⁻) and Nitrate (NO₃⁻)</strong>: Indicators of biofilter efficiency</li>
  </ul>

