<div class="text-base text-gray-700 leading-relaxed">
  <p>Maintaining optimal water quality is critical to the success of any Recirculating Aquaculture System (RAS). Poor water conditions can lead to stress, disease, and even fish mortality, making water quality management one of the most important tasks in aquaculture operations.</p>

  <h2 class="text-2xl font-bold mt-6 mb-2">Key Parameters to Monitor</h2>
  <div class="overflow-x-auto my-4">
    <table class="min-w-full text-sm border border-gray-300">
      <thead class="bg-gray-100">
        <tr>
          <th class="p-3 border">Parameter</th>
          <th class="p-3 border">Optimal Range</th>
          <th class="p-3 border">Impact if Out of Range</th>
        </tr>
      </thead>
      <tbody>
        <tr><td class="p-3 border">Temperature</td><td class="p-3 border">22–28°C (species dependent)</td><td class="p-3 border">Reduced growth, stress</td></tr>
        <tr><td class="p-3 border">pH</td><td class="p-3 border">6.8–7.5</td><td class="p-3 border">Affects ammonia toxicity</td></tr>
        <tr><td class="p-3 border">Ammonia</td><td class="p-3 border">&lt;0.05 mg/L</td><td class="p-3 border">Toxic to fish</td></tr>
        <tr><td class="p-3 border">Nitrite</td><td class="p-3 border">&lt;0.1 mg/L</td><td class="p-3 border">Reduces oxygen transport</td></tr>
        <tr><td class="p-3 border">Dissolved Oxygen</td><td class="p-3 border">&gt;6 mg/L</td><td class="p-3 border">Essential for respiration</td></tr>
      </tbody>
    </table>
  </div>

  <h2 class="text-2xl font-bold mt-6 mb-2">Control Methods</h2>
  <ul class="list-disc pl-6">
    <li><strong>Oxygenation systems</strong> to maintain DO levels</li>
    <li><strong>pH buffering agents</strong> like sodium bicarbonate</li>
    <li><strong>Regular monitoring</strong> with sensors and automation</li>
    <li><strong>Biofilters</strong> to control ammonia and nitrite</li>
    <li><strong>Backup systems</strong> for power outages and mechanical failure</li>
  </ul>

  <h2 class="text-2xl font-bold mt-6 mb-2">Conclusion</h2>
  <p>Effective water quality management ensures healthy fish, efficient feed conversion, and long-term sustainability. Regular monitoring and immediate intervention are crucial to maintaining system balance and operational success.</p>
