<p>
  Kimyasal depolama tanklarında et kalınlığı; tank çapı, çalışma sıcaklığı, yoğunluk, statik kolon yüksekliği ve 
  proses türüne göre belirlenir. PP/PE malzemelerde sıcaklık arttıkça mekanik dayanım düşer; bu nedenle termal dengeleme faktörleri kullanılır.
</p>

<p>
  CEKA Endüstriyel her projede gerilme analizi, uzun süreli sünme katsayısı ve güvenlik faktörü (SF 2.0–3.0) ile optimum 
  et kalınlığını belirler. Karıştırıcı veya lokal noktalar için ekstra ring veya gömlek saç takviyesi uygulanır.
</p>

